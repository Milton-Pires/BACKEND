public class Localizacao{
    private String bloco;
    private String andar;
    private String sala;
    private String descricao;
    
    public Localizacao(String bloco, String andar, String sala, String descricao) {
        this.bloco = bloco;
        this.andar = andar;
        this.sala = sala;
        this.descricao = descricao;
    }

    
}